     public void displayData(){
        try{
            Class.forName("com.mysql.cj.jdbc.Driver"); //Register the mysql driver
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/rentledger","root","shiyashetty");
            
            Statement st=con.createStatement();
            String sql = "select r.r_id,t.name,t.mobile,r.room_no,r.size from tenant t,rooms r  where r.r_id = t.t_id and availibility='Unavailable'";
            ResultSet rs=st.executeQuery(sql);
            DefaultTableModel Model=(DefaultTableModel)table1.getModel();
            Model.setRowCount(0);
            while(rs.next()){
                String r_id = String.valueOf(rs.getInt("r_id"));
                String name = rs.getString("name");
                String mobile = rs.getString("mobile");
                String room_no = rs.getString("room_no");
                String size = rs.getString("size");
                
                String tbData[]={r_id,name,mobile,room_no,size};
                
                
                Model.addRow(tbData);
     
            }
            con.close();
        }
        catch(Exception e){
            System.out.println(e.getMessage());
        }
        }
    
