    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
       
        int row =table1.getSelectedRow();
        if (row != -1) {
        String cell=table1.getModel().getValueAt(row,0).toString();
        
         try {
            Class.forName("com.mysql.cj.jdbc.Driver"); // Register the MySQL driver
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/rentledger", "root", "shiyashetty");

            // Assuming you have a 'payment' table with columns 'date' and 'amount' for payment history
            String sql = "SELECT date, amount FROM payment WHERE t_id =" + cell;
            PreparedStatement pst = con.prepareStatement(sql);
            // Assuming 'getId()' returns the person's ID

            ResultSet rs = pst.executeQuery();

            // Display payment history data in a table
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Date");
            model.addColumn("Amount");

            while (rs.next()) {
                String date = rs.getString("date");
                double amount = rs.getDouble("amount");
                model.addRow(new Object[]{date, amount});
            }

            // Close resources
            rs.close();
            pst.close();
            con.close();

            // Create and display the payment history dialog form with the table data
            paymentdialog paymentDialog = new paymentdialog(new javax.swing.JFrame(), true, cell);
            paymentDialog.getJTable().setModel(model); // Assuming jTable1 is the name of your table in paymentdialog
            paymentDialog.setVisible(true);

        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Error retrieving payment history: " + e.getMessage());
        }

        }
        else {
        // Display a message or handle the case where no row is selected
            JOptionPane.showMessageDialog(this, "Please select a row to view payment history.");
        } 
       
        
        
        
        
    }//GEN-LAST:event_jButton8ActionPerformed

